    public void renderTableKhuyenMai() {
        ArrayList<ChuongTrinhGiamGiaDTO> danhSachCTGG = chuongTrinhGiamGiaBUS.getDanhSachCTGG();
        model.setRowCount(0);
        for (ChuongTrinhGiamGiaDTO ctgg : danhSachCTGG) {
            
            if (ctgg.getTrangThai() == 0) {
                model.addRow(new Object[]{ctgg.getIDGiamGia(), ctgg.getTenCT(), ctgg.getLoaiCT(), chuongTrinhGiamGiaBUS.dateFormat(ctgg.getThoiGianKhoidau()), chuongTrinhGiamGiaBUS.dateFormat(ctgg.getThoiGianKetthuc())});
            }
        }
    }
    
