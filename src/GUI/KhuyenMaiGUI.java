    private void btnSaveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSaveMouseClicked
        // TODO add your handling code here:
        String IDGiamGia = txtMaGG.getText();
        String LoaiChuongTrinh = txtLoaiCT.getText();
        String TenCT = txtTenCT.getText();
        String ngayBD = txtNgayBD.getText();
        String ngayKT = txtNgayKT.getText();
        try {
            ChuongTrinhGiamGiaDTO chuongTrinhGiamGiaDTO = new ChuongTrinhGiamGiaDTO(IDGiamGia, TenCT, LoaiChuongTrinh, chuongTrinhGiamGiaBUS.dateFormatString(ngayBD), chuongTrinhGiamGiaBUS.dateFormatString(ngayKT), 0);
            if (chuongTrinhGiamGiaBUS.addChuongTrinhGG(chuongTrinhGiamGiaDTO)) {
                renderTableKhuyenMai();
            }
        } catch (ParseException ex) {
            Logger.getLogger(KhuyenMaiGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveMouseClicked

    private void btnDeleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDeleteMouseClicked
        // TODO add your handling code here:
        if (chuongTrinhGiamGiaBUS.deleteChuongTrinhGG(txtMaGG.getText())) {
            JOptionPane.showMessageDialog(null, "XÓA THÀNH CÔNG");
            resetValue();
            renderTableKhuyenMai();
        }  else {
            JOptionPane.showMessageDialog(null, "XÓA KHÔNG THÀNH CÔNG");
        }
    }//GEN-LAST:event_btnDeleteMouseClicked

    private void btnUpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnUpdateMouseClicked
        // TODO add your handling code here:
        String IDGiamGia = txtMaGG.getText();
        String TenCT = txtTenCT.getText();
        String LoaiChuongTrinh = txtLoaiCT.getText();
        String ngayBD = txtNgayBD.getText();
        String ngayKT = txtNgayKT.getText();
        try {
            ChuongTrinhGiamGiaDTO chuongTrinhGiamGiaDTO = new ChuongTrinhGiamGiaDTO(IDGiamGia, TenCT, LoaiChuongTrinh, chuongTrinhGiamGiaBUS.dateFormatString(ngayBD), chuongTrinhGiamGiaBUS.dateFormatString(ngayKT), 0);

            if (chuongTrinhGiamGiaBUS.updateChuongTrinhGG(chuongTrinhGiamGiaDTO)) {
                JOptionPane.showMessageDialog(null, "CẬP NHẬT THÀNH CÔNG");
                renderTableKhuyenMai();
            }  else {
                JOptionPane.showMessageDialog(null, "CẬP NHẬT CHƯƠNG TRÌNH KHÔNG THÀNH CÔNG");
            }
        } catch (ParseException ex) {
            Logger.getLogger(KhuyenMaiGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnUpdateMouseClicked

