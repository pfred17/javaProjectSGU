    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        try {
            if (txtNgayBD.getText().isEmpty()) {
                renderTableHoaDonTimKiem(hoaDonBUS.getDanhSachHD());
                return;
            }
            Date ngayBD = dateFormat.StringToDate(txtNgayBD.getText());
            Date ngayKT = dateFormat.StringToDate(txtNgayKT.getText());
            String key = txtTimKiem.getText();

            ArrayList<HoaDonDTO> danhSachHoaDon = hoaDonBUS.getDanhSachTiemKiem(ngayBD, ngayKT, key);
            if (danhSachHoaDon != null) {
                renderTableHoaDonTimKiem(danhSachHoaDon);
            } else {
                JOptionPane.showMessageDialog(null, "Không tìm thấy hóa đơn nào!");
            }

        } catch (ParseException ex) {
            Logger.getLogger(HoaDonGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

