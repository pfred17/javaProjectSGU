    public void renderTableHoaDon() {
        ArrayList<HoaDonDTO> danhSachHoaDon = hoaDonBUS.getDanhSachHD();
        model.setRowCount(0);
        for (HoaDonDTO hoaDon : danhSachHoaDon) {
            if (hoaDon.getTrangThai() == 0) {
                model.addRow(new Object[]{hoaDon.getIDHoaDon(), hoaDon.getIDKhachHang(), hoaDon.getIDNhanVien(), dateFormat.dateToString(hoaDon.getNgayLapHoaDon()), priceFormat.formatDonGia(hoaDon.getTienGiamGia()), priceFormat.formatDonGia(hoaDon.getTongTien())});
            }
        }
    }

    public void renderTableHoaDonTimKiem(ArrayList<HoaDonDTO> danhSachHoaDon) {
        model.setRowCount(0);
        for (HoaDonDTO hoaDon : danhSachHoaDon) {
            model.addRow(new Object[]{hoaDon.getIDHoaDon(), hoaDon.getIDKhachHang(), hoaDon.getIDNhanVien(), dateFormat.dateToString(hoaDon.getNgayLapHoaDon()), priceFormat.formatDonGia(hoaDon.getTienGiamGia()), priceFormat.formatDonGia(hoaDon.getTongTien())});
        }
    }

