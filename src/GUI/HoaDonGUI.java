    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        try {
            String IDHoaDon = txtMaHoaDon.getText();
            String IDKhachHang = txtMaKhachHang.getText();
            String IDNhanVien = txtMaNhanVien.getText();
            Date NgayTao = dateFormat.StringToDate(txtNgayTao.getText());
            double TienGiamGia = Double.parseDouble(txtTienGiamGia.getText());
            double TongTien = Double.parseDouble(txtTongTien.getText());
            HoaDonDTO hoaDon = new HoaDonDTO(IDHoaDon, IDKhachHang, IDNhanVien, "CGG001", NgayTao, TongTien, TienGiamGia, 0);
            if (hoaDonBUS.updateHoaDon(hoaDon)) {
                JOptionPane.showMessageDialog(null, "Sửa thành công!");
                renderTableHoaDon();
                resetValue();
            }

        } catch (ParseException ex) {
            Logger.getLogger(HoaDonGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

