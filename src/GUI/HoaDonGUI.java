    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        String IDHoaDon = txtMaHoaDon.getText();
        if (hoaDonBUS.deleteHoaDon(IDHoaDon)) {
            int result = JOptionPane.showConfirmDialog(this, "Bạn muôn xóa hóa đon này ?", "Xác nhận", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_NO_OPTION) {
                renderTableHoaDon();
                JOptionPane.showMessageDialog(null, "Đã xóa hóa đơn: " + IDHoaDon);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

